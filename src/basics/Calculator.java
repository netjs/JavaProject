package basics;

public class Calculator {
	public double multiplication(double i, double j) {
		return i * j;
	}
	
	public double addition(double i, double j) {
		double sum = i + j;
		return sum;
	}
	
	public double subtraction(double i, double j) {
		return i - j;
	}
	
	public double division(double i, double j) {
		return i/j;
	}
}
